class Product{
  final int id;
  final String name;
  final String categories;
  final String image;
  final String decoration;
  final String price;
  int quantity;
  Product({
    required this.id,
    required this.name,
    required this.categories,
    required this.image,
    required this.decoration,
    required this.price,
    required this.quantity,
  });
}